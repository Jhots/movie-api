CREATE TABLE MOVIE
(
    MOVIE_ID   NUMBER(24,0) GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 1 INCREMENT BY 1 MINVALUE 0 NOMAXVALUE NOT NULL,
    LEGACY_ID  NUMBER(24,0) NOT NULL,
    PUBLIC_ID  VARCHAR(36)  NOT NULL,
    MOVIE_NAME VARCHAR(100) NOT NULL,
    CONSTRAINT movie_pk PRIMARY KEY (MOVIE_ID) USING INDEX TABLESPACE TSIMOVIE01
);

COMMENT
ON TABLE MOVIE IS '[NOT_SECURITY_APPLY] Table to store information about movies from the API';
COMMENT
ON COLUMN MOVIE.MOVIE_ID IS '[NOT_SECURITY_APPLY] Primary key identifier for internal database queries';
COMMENT
ON COLUMN MOVIE.LEGACY_ID IS '[NOT_SECURITY_APPLY] Identifier used for API backward compatibility in V1';
COMMENT
ON COLUMN MOVIE.PUBLIC_ID IS '[NOT_SECURITY_APPLY] More secure identifier for API user queries';
COMMENT
ON COLUMN MOVIE.MOVIE_NAME IS '[NOT_SECURITY_APPLY] Field used to store the name of the movie.';

CREATE INDEX MOVIE_IDX01 ON MOVIE (MOVIE_NAME) TABLESPACE TSIMOVIE01;
CREATE INDEX MOVIE_IDX02 ON MOVIE (PUBLIC_ID) TABLESPACE TSIMOVIE01;