CREATE TABLE ROLES
(
    ROLE_ID   NUMBER(24,0) GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 1 INCREMENT BY 1 MINVALUE 0 NOMAXVALUE NOT NULL,
    ROLE_NAME VARCHAR2(50) NOT NULL,
    CONSTRAINT ROLES_PK PRIMARY KEY (ROLE_ID)
);

COMMENT
ON TABLE ROLES IS '[NOT_SECURITY_APPLY] Table to store roles (functions) that can be assigned to users.';
COMMENT
ON COLUMN ROLES.ROLE_ID IS '[NOT_SECURITY_APPLY] Primary key identifier for internal database queries.';
COMMENT
ON COLUMN ROLES.ROLE_NAME IS '[NOT_SECURITY_APPLY] Field used to store the name of the role.';

CREATE INDEX ROLES_IDX01 ON ROLES (ROLE_NAME);
